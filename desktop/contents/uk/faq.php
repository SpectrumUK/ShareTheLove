<h4>Can I deep Fry frozen foods?</h4>
<p>Yes. Just remember to shake off any excess ice before frying them.</p>
<h4>Can I re-use the oil from my Deep Fat Fryer?</h4>
<p>Yes, but you should allow the oil to cool, then pour the oil through muslin or absorbent paper before re-using. However oil should be completely changed after every 8-10 uses.</p>
<h4>How do I clean my Fryer?</h4>
<p>Never start cleaning until the oil has cooled down. The basket, bowl and lid should all be removed (Please note that on some models the bowl is not removable) and soaked in hot soapy water for 20 minutes.</p>
<p>The basket should then be cleaned with a stiff brush and the bowl with a non-abrasive cleaner. The outer body should be wiped with a damp cloth.</p>
<h4>Why is corn oil recommended for Fryers?</h4>
<p>Other oils may cause foaming and unpleasant smells at higher temperatures. Corn oil will give up to 8-10 uses whereas other oils should be changed after 3-4 fries.</p>
<h4>Why has the food become soft and sticky?</h4>
<p>You should wait until the oil reaches a high enough temperature before frying; the optimum temp is 190&deg;C.</p>
<h4>Why is the oil overflowing during the frying process?</h4>
<p>Answer 1: This usually happens when the oil has been used several times and has a poor quality. The oil should be replaced asap.</p>
<p>Answer 2: Adding watery and refrigerated food materials can lead to this problem.</p>
