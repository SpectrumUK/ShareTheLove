<h4>Can I throw away my appliance with the normal household waste at the end of its life?</h4>
<p>No, hand it in at an official collection point for recycling. to help preserve the environment. You can also return it to us and we will recyle it for you. Our addresses for recycling are WEEE Recycling Department, Russell Hobbs Ltd, Heathmill Road, Wombourne, Wolverhampton WV5 8AQ or WEEE Recycling Department, Russell Hobbs Ltd, Fir Street, Failsworth, Manchester, M35 0HS.</p>
<h4>How can I clean the outside of the appliance?</h4>
<p>Clean the outside of the coffeemaker with a soft cloth moistened with warm water. Filters, filter holders and carafes can be cleaned in warm soapy water, rinsing thoroughly afterwards to remove all traces of soap. Do not immerse the coffeemaker or its cord in water and do not use abrasive cleaners or scourers to clean the appliance.</p>
<h4>Why does the fuse blow when I switch on the coffeemaker or during use?</h4>
<p>You may have connected too many appliances to the same circuit. Unplug a number of other appliances connected to the same circuit and then plug the coffee maker in again. If the fuse continues to blow, please contact the Russell Hobbs Customer Service Department on 0845 658 9700.</p>
<h4>Why doesn't my coffee taste good?</h4>
<p>If the taste of your coffee is not good from the start, please keep in mind that:</p>
<ol>
<li>You should at least brew the minimum amount of coffee as indicated on the jug and water container.</li>
<li>The brand of coffee can influence the taste.</li>
<li>You might want to try a different blend of coffee for a different taste.</li>
<li>Use fresh water to brew your coffee.</li>
<li>Keep your coffee machine clean. Regular cleaning of the filter, holder and jug will have a positive influence on your coffee taste.</li>
</ol>
<p>If the taste of your coffee becomes less after some time of brewing:</p>
<ul>
<li>The coffee might have stayed on the hot plate too long. Coffee becomes strong and bitter if you leave it on the hot plate for too long, especially if the jug contains only a small quantity of coffee.</li>
</ul>
<p>If these answers do not solve your problem, please contact the Russell Hobbs Customer Service Department on 0845 658 9700.</p>
<h4>Why does my coffee not have the desired strength?</h4>
<p>The amount of ground coffee put in the filter influences the coffee strength. In general, 1 level coffee spoon is enough for 1 small cup of coffee. Depending on your strength preference, you can use less ground coffee for a milder taste and more ground coffee for a stronger taste.</p>
<p>If this solution does not solve your problem you can contact the Russell Hobbs Customer Service Department on 0845 658 9700.</p>
<h4>Why is the coffee not hot enough?</h4>
<p>If you have used the coffeemaker for quite some time and the temperature of the coffee decreases, we advise you to descale the coffeemaker.</p>
<h4>Do I have to descale my appliance?</h4>
<p>Yes. Descaling is an important part of the maintenance of your appliance. You should descale your appliance regularly - at least monthly.</p>
<p>To descale your appliance, you can use any descale agent suitable for use in plastic bodied products or use white vinegar. Do not use cleaning vinegar.</p>
<p>Descaling will take half an hour to an hour to complete. Go through the following steps:</p>
<ol>
<li>Fill the water tank with 0,8 litre of white vinegar or follow the instructions on the package of your descaler.</li>
<li>There is no need to put a paper filter or coffee grind into the appliance.</li>
<li>Switch on the appliance and let it complete one brewing cycle.</li>
<li>Note: For thorough descaling, repeat step 1 to 3 to make sure no scale is left in the appliance.</li>
<li>When using vinegar, let the appliance complete two more cycles with fresh, cold water to remove all vinegar from the appliance. For a descaler, follow the instructions on the package, but at least complete one cycle with fresh, cold water.</li>
</ol>
<p>Descaling your appliance can help you to:</p>
<ul>
<li>Ensure the good quality of your brewed coffee.</li>
<li>Keep the brewing time constant.</li>
<li>Extend the lifetime of your appliance.</li>
<li>Limit the chances your appliance might break down</li>
</ul>
<h4>The coffeemaker continues to drip long after the brewing cycle has ended. Is this normal?</h4>
<p>Some dripping is normal and is caused by condensation of steam, so there is no need to worry.</p>
<h4>There is water/condensation on the lid of the coffeemaker?</h4>
<p>The appliance generates steam during the brewing cycle. Therefore a small amount of condensation on the lid of the coffee maker is normal and does not cause problems.</p>
<h4>Why does the coffeemaker not work after I switch it on?</h4>
<p>If the coffeemaker is not working after you have switched it on, please check if the plug of the coffeemaker is properly inserted in the wall socket and if the wall socket is working.</p>
<p>If this solution does not solve your problem please contact the Russell Hobbs Customer Service Department on 0845 658 9700.</p>
<h4>How do I get a more consistent coffee quality?</h4>
<p>Check that the filter holder hasn't fallen. Simply add more coffee to the filter!</p>
<h4>How can I brew hotter coffee?</h4>
<p>Here are some tips to brew hotter coffee:</p>
<ul>
<li>Check if the water heater is working properly: draw off some water from the steam tube. If the water comes out hot, the water heater is OK. If the water is cold, please contact the Russell Hobbs Customer Service Department on 0845 658 9700.</li>
<li>Check the temperature of the cups. Cold cups will very quickly lower the temperature of the coffee. Try warming cups with some hot water and/or steam or by keeping them on the cup warming plate (if present).</li>
<li>Pre-heat the water circuit and filter by first dispensing one cup of coffee without putting in any coffee, so by dispensing only hot water. After that, start brewing coffee.</li>
</ul>
