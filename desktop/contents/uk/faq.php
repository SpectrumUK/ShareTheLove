<h3 class="faq-header">Bread Makers</h3>
<!-- sof faq content -->
<div class="faq-content bread-makers"><!-- strip image --> <span class="strip-img bread-makers-strip"></span> <!-- faq text -->
<div class="faq-text">
<h4>I am lacto intolerant. (Lactose intolerance is basically an allergy to the sugar, which is found in milk). What substitute can I use for the milk in the recipes?</h4>
<p>Non-Dairy milk products such as soya milk, rice milk etc may be used in equal measures to replace milk in bread recipes. Powdered soya milk or tofu milk powder may also be used instead of non-fat dry milk but you must follow the package instructions.</p>
<p>(Also, equal measures of vegetable cooking water, fruit juices, vegetable juices, flat beer and plain water are all viable substitutions for milk in most recipes but customers would have to experiment with these).</p>
<h4>Loaf is very small / heavy &amp; dense</h4>
<p>Answer 1: Yeast - Is the yeast being used correctly? If it is in a large box, packet or tin it may need to be pre activated with water first (please refer to packaging instructions).</p>
<p>If it is in small sachets then it probably won&rsquo;t need mixing first but please check packaging instructions).</p>
<p>Is the yeast still in date or if in sachet form was it opened more than a day before? If either of these are the case then the yeast will have been inactive and the bread won&rsquo;t have risen.</p>
<p>Answer 2: Correct Programme - Has the correct program and weight setting been keyed into the machine. For example if the ingredients for a 1kg were loaded in and a 500g weight setting chosen then the cycle will have been too short and the bread wouldn&rsquo;t be very risen.</p>
<p>Answer 3: Conditions - Was the bread maker left in a draft or a very cold room? Was the bread maker lid opened during the rising and baking cycle.</p>
<p>Answer 4: Other ingredients - Was sufficient yeast, sugar and water used? Were the ingredients added in the correct order, i.e liquids first, yeast last? If the yeast was in contact with the water too soon then it could become inactive.</p>
<h4>Loaf is sunken on top</h4>
<p>Answer 1: Liquid - Was there too much liquid used? If this happens regularly we would recommend that you start reducing the water down by approximately a dessertspoon at a time until the desired results are achieved.</p>
<p>Answer 2: Other ingredients - Was too much sugar or yeast added? This would cause the bread to over rise and then collapse.</p>
<p>Was there enough salt added? Salt prevents bread from over rising and if insufficient salt was added this can result in a loaf with a sunken top.</p>
<p>Were strong / pungent ingredients used such as garlic or cinnamon? These are not very 'yeast friendly' and so only small amounts should be used. Try slightly reducing the amounts used next time.</p>
<p>Answer 3: Conditions - Was the lid opened during the rising or baking process? Opening the lid would cause the heat to escape making the bread 'dome' collapse.</p>
<p>Was the bread maker moved or knocked whilst operating? This could have disturbed the process and made the bread collapse.</p>
<h4>Bread Surface Is Very Uneven / Knarled &amp; Knotty</h4>
<p>Liquid: Was sufficient liquid added &ndash; Too little and the dough won't form into an even shape. Always check that the dough is forming into a smooth, slightly tacky ball.</p>
<p>If the dry ingredients aren't being picked up then add a few more drops of water as it is mixing.</p>
<h4>Bread Is Soggy Or Too Moist In The Centre</h4>
<ol>
<li>Cutting too soon: Was the bread sliced straight away? The bread should always be allowed to cool and dry out on a wire rack for at least 20 minutes after baking. If it is cut into straight away, the steam escaping will make the bread soggy.</li>
<li>Added ingredients: Were moist ingredients used such as bananas, apple sauce, yogurt? These can make the centre of the bread soggy so we would suggest reducing amount used down by a teaspoon at a time until desired results are achieved.</li>
<li>Humidity: Was it a very humid day? &ndash; Humidity can make dough too moist. If the bread maker is used on a very humid day, we would suggest you add an extra tablespoon of flour to the ingredients.</li>
</ol>
<h4>What is the best yeast to use in the Bread maker?</h4>
<p>Any standard dried yeast is fine. As well as this, you can get the type in a tin or large packet which needs pre-activating with water before use; (Allison's in a tin is an example).</p>
<p>The yeast found in small sachets however is ready to use and does not need mixing with water beforehand (it is 'live' hence being in small amounts).</p>
<p>You also get fast action yeast which is about 3 times more potent than standard dried yeast and so you can use it but you won't need as much.</p>
<p>We don't really advise using fresh yeast, as the results are more unpredictable. Our Home Economist generally recommends Allison's "Easy Bake", which is found in small sachets and is usually available in supermarkets. The results from this yeast are normally very good and it is very easy to use.</p>
<h4>Where can I obtain further recipes for my Bread maker.</h4>
<p>There is a book called "More Electric Bread" by Innovative Cooking Enterprises, but information/recipes can also be found on the internet.</p>
