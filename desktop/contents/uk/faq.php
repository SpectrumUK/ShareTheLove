<h4>Dirty water and impurities come out of the soleplate during ironing, what should I do?</h4>
<p>Self clean is required or has not been carried out correctly - Refer to the instruction booklet for self clean instructions.</p>
<p>Descaling required (if not a self clean iron) - Descale using a suitable descaler for plastic parts and follow instructions on the packet.</p>
<p>The iron may also require new filter cartridges.</p>
<h4>Is it necessary to empty the water tank after each use?</h4>
<p>Yes, we recommend you always empty the water tank after use and store the iron in an upright position to avoid corrosion and damage to the soleplate.</p>
<h4>The steam generator does not produce any steam. What can I do?</h4>
<p>There is an airlock in the unit - Prime the unit by puting the generator unit on the floor and hold the iron itself up higher. Make sure that you have the heat and steam settings on 'maximum' then keep your finger on the trigger for about 5 &ndash; 6 minutes without letting go and try shaking the wire. This will not damage the unit and we only advise our customers to do this if they have an air lock.</p>
<p>Whilst carrying out the above you will hear the usual pulsating noise as normal, which should go quieter as the shot of steam works its way through the unit.</p>
<p>You can also try to prime the unit the opposite way round by having the generator unit on a high surface holding the iron at a lower level.</p>
<h4>Why does the steam output get weaker?. What should I do?</h4>
<p>It is common for the Steam generator to experience dip in steam pressure/ output when the water is being pumped from water tank to the boiler. This dip is momentarily and it restores to normal rate.</p>
<p>It is also common for the initial burst of steam to be higher and it tapers off to a steady level with continuous triggering of the steam trigger.</p>
<p>This is due to the initial release of build up pressure inside the boiler and this pressure will drop to a steady state level when the steam trigger is continuously depressed. It builds up pressure again when the trigger is released.</p>
<p>If steam output does not recover, kindly check steam generator panel for any notifications and take remedial action. e.g. watertank empty light or steam generator gone into standby mode.</p>
<h4>What type of water should I use?</h4>
<p>Your appliance has been designed to be used with tap water.</p>
<p>In case you live in an area with very hard water, fast scale build-up may occur. Therefore, it is recommended to mix 50% distilled water and 50% tap water, or to use only distilled water to prolong the lifetime of your appliance.</p>
<p>Do not put perfume, vinegar, starch, descaling agents, ironing aids or other chemicals in the water tank, as your appliance is not designed to work with these chemicals.</p>

<h4>The water at home goes through a softener, will this have any affect inside the machine?</h4>
<p>There are different types of water softener, if it is a filter it is OK.</p>
<p>If it is adding chemicals or addicitives into the water, it may damage the Steam Generator and give a poor steam performance.</p>
