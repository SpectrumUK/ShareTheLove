<h4>Can I heat up other liquids than water in my kettle?</h4>
<p>No, the kettle is intended for heating water only. Do not use it to heat up any other liquids.</p>
<h4>Why does the noise of the kettle increase as the kettle comes to boil?</h4>
<p>The water in this kettle is heated up rapidly, which causes the noise. This is a normal phenomenon and there is nothing wrong with your kettle.</p>
<h4>How can I clean the outside of the kettle?</h4>
<p>Clean the outside of the kettle and the base with a warm damp soft cloth. Do not let the moist cloth come into contact with the cord, the plug and the connector of the base.</p>
<p>Never immerse the kettle or its base in water and do not use abrasive cleaning agents such as scourers to clean the kettle.</p>
<h4>What are the white spots on the bottom of my kettle?</h4>
<p>Depending on the hardness of the water where you live, small spots may appear on the heating element of your kettle when you use it. This is the result of scale build-up on the heating element and on the inside of the kettle over time.</p>
<p>The harder the water, the faster scale builds up. Scale can occur in different colours and although scale is harmless, too much scale can influence the performance of your kettle. Descale your kettle regularly to pro long the life of your kettle.</p>
<h4>Can I clean my anti-scale filter in the dishwasher?</h4>
<p>We don't recommend that you clean your anti-scale filter in a dishwasher as this could cause damage. To clean your filter rinse the filter under a running tap whilst brushing with a soft brush.</p>
<h4>Can I use the kettle without the anti-scale filter?</h4>
<p>We recommend that you don't use the kettle without fitting the filter as the kettle may not switch off automaticaly if the filter isn't fitted. Scale may also give your drink a powdery taste.</p>
<p>The scale filter prevents scale particles from ending up in your drink and has a mesh that attracts scale from the water. Scale is not harmful to your health.
Removing the scale filter can also reduce the performance of your kettle.</p>
<h4>Do I need to descale my kettle?</h4>
<p>Yes, you have to descale your kettle regularly. Minerals present in tap water will cause scale to build up inside your kettle when water is heated.</p>
<p>Regularly descaling your kettle will help to: reduce the boiling time and save energy, keep the inside of the kettle clean and extend the kettles life.</p>
<p>Regular descaling prolongs the life of the kettle. We recommend that you descale regularly - at least monthly.</p>
<p>You must only use a descaler that is suitable for use in plastic products. If you use a descaling agent, follow the instructions on the bottle or package to descale your kettle.</p>
<h4>The kettle doesn't work. What should I do?</h4>
<p>This problem may have multiple causes:</p>
<ol>
<li>The appliance is not connected to the mains.</li>
<li>The dry-boil protection has been activated.</li>
<li>The kettle needs to be descaled.</li>
</ol>
<p>Below you can find the solutions for each of these causes.</p>
<p>Solution Cause 1: The appliance is not connected to the mains. 
Check if the plug of the kettle has been inserted properly into the wall socket and if the wall socket is working.</p>
<p>Solution Cause 2: The dry-boil protection has been activated. 
The dry-boil protection is a safety feature that automatically switches off the kettle. It will be activated if the kettle is switched on with insufficient or no water inside or if all of water inside the kettle has evaporated during operation.</p>
<p>To reset the dry-boil protection, you have to lift the kettle from its base and fill it with cold water. When you place the kettle back on the base, the dry-boil protection is reset and you can switch the kettle on again.</p>
<p>Solution Cause 3: The kettle needs to be descaled. Regularly descaling your kettle will help to: reduce the boiling time and save energy, keep the inside of the kettle clean and extend the kettles life.</p>
<p>Regular descaling prolongs the life of the kettle. We recommend that you descale regularly - at least monthly.</p>
<p>You must only use a descaler that is suitable for use in plastic products. If you use a descaling agent, follow the instructions on the bottle or package to descale your kettle.</p>
<p>If the above solution(s) did not help to solve your problem, please contact the Russell Hobbs Consumer Service Department on 0845 658 9700.</p>
<h4>Why is water ejected from the spout during heating up?</h4>
<p>This indicates that the kettle has been filled beyond the maximum level indication.</p><p>For safety reasons, you should never fill the kettle beyond the MAX indication on your water level indication window.</p>
<h4>There is water/condensation on the base of the kettle. Is this normal?</h4>
<p>If you find some condensation or a few drops of water on the base, this is not a manufacturing defect but a normal occurrence.</p><p>However, if you see more water than just a few drops on the base or under the base, please contact the Russell Hobbs Consumer Service Department on 0845 658 9700.</p>
<h4>The water boiled in this kettle tastes metallic. How can I improve the taste?</h4>
<p>Try descaling the kettle according to the descale procedure. Rinse carefully afterwards.</p><p>If this does not help, please contact the Russell Hobbs Consumer Service Department on 0845 658 9700.</p>
<h4>Why does it take a longer time for the water to boil?</h4>
<p>If the water boiling procedure has slowed down, the heating element of the kettle is probably covered with scale. Descale the kettle to remove the scale and revert back to quick boiling.</p>
<h4>How can I descale the kettle and how often should I do this?</h4>
<p>You must only use descaler suitable for use in plastic products. If you use a descaling agent, follow the instructions on the bottle or package to descale your kettle.</p><p>Regular descaling prolongs the life of the kettle. Kettles should be descaled regularly - at least monthly.</p><p>Contact your local or regional water board for information on the hardness of your tap water. If you use the kettle more than 5 times a day, you may have to descale it more often.</p><p>Regularly descaling your kettle will help to pro long the life of your kettle, keep the inside of the kettle clean and reduce boiling times which will then reduce energy consumption.</p>
<h4>Can I throw away my kettle with the normal household waste at the end of its life?</h4>
<p>No, to preserve the environment please hand it in at an official collection point for recycling or you can return to us  and we will recyle it for you.</p><p>Our addresses for recycling is; WEEE Recycling Department, Russell Hobbs Ltd, Heathmill Road, Wombourne, Wolverhampton WV5 8AQ or WEEE Recycling Department, Russell Hobbs Ltd, Fir Street, Failsworth, Manchester, M35 0HS.</p>
<h4>What happens when I accidentally switch on the kettle with no water or not enough water?</h4>
<p>This kettle is equipped with boil-dry protection. The kettle will automatically switch off if it is accidentally switched on when there is no water or not enough water in it.</p><p>To use it again lift the kettle from its base and fill it with cold water to reset boil-dry protection. The kettle is now ready for use again.</p>
<h4>The kettle smells weird, how can I remove the smell?</h4>
<p>Try descaling the kettle according the descale procedure, as this removes most smells. Rinse carefully afterwards.</p><p>If this does not help, please contact the Russell Hobbs Customer Service Department on 0845 658 9700.</p>
