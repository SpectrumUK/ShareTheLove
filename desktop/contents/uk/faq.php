<h4>My iron doesn't produce any steam.</h4>
<p>Answer 1: The steam control may have been set to the no steam position. Set the steam control to a steam position.</p>
<p>Answer 2: There is no water in the tank and/or the settings are not set correctly.</p>
<p>Solution:</p>
<p>Step 1: Ensure there is water in the tank.</p>
<p>Step 2: Set to the appropriate steam and temperature setting - When steam ironing, you must set the steam control to steam and the temperature control to a temperature equal to or higher than the steam symbol on the dial.</p>
<h4>The mains power switches off when I switch my iron on or when the iron is in use. Can the appliance cause a power failure?</h4>
<p>Step 1: Check that the connection to the plug is secure and not loose and also check how many appliances are using the same output. Failure could be caused by over current in your mains.</p>
<p>Step 2: Check the iron's power cord for visible signs of damage (e.g. kinks, flaring of the braiding). Remove the rest of the appliances and switch it on again.</p>
<p>Step 3: if the mains power goes off again, contact the Russell Hobbs Customer Service Department on 0845 658 9700.</p>
<h4>The steam boost function or vertical steam boost function on my iron doesn't work.</h4>
<p>Answer 1: The (vertical) steam boost function has been used too often within a very short period.</p>
<p>Put the iron in a horizontal position and wait for a while before using the (vertical) steam boost function again.</p>
<p>Answer 2: The iron is not hot enough. Set the temperature dial to a temperature suitable for steam ironing.</p>
<h4>The Spray function or Spray button on my iron isn't working (properly).</h4>
<p>Make sure there is enough water in the tank and check that the spray button is able to spring back on its own after pressing.</p>
<p>If the spray nozzle hole is blocked, you can clean it carefully with a sharp needle or pin.</p>
<h4>The indicator light isn't working (properly).</h4>
<p>Make sure the iron is plugged into a working mains power and set the temperature dial to a desired position.</p>
<p>It is normal for the orange pilot light to turn on and off during ironing. It comes on as the iron is heating up, and goes off when it is hot enough.</p>
<h4>Smoke comes out of the appliance.</h4>
<p>Some parts of the appliance have been slightly greased and as a result, the appliance may produce some smoke when it is switched on for the first time.</p>
<p>Also, if there is remaining water in the soleplate from the last usage, the smoke observed may actually be water evaporating. However, these will stop after a short while.</p>
<h4>The soleplate coating is scratched/worn away.</h4>
<p>Your iron has multiple thin layers of coating and light scratches will not impede the performance of the iron. You can continue using the iron as per normal.</p>
<h4>My iron leaves shiny patches on fabrics.</h4>
<p>Fabrics can acquire shiny patches if they are ironed at too high temperatures. Try a lower temperature setting.</p>
<p>Also, to prevent shiny patches, you can put a cloth (e.g. a tea towel) between the iron and the fabric.</p>
<h4>What type of water should I use?</h4>
<p>Your appliance has been designed to be used with tap water.</p>
<h4>Water drips from the holes of the soleplate during ironing. What should I do?</h4>
<p>There may be several reasons for this:</p>
<ol>
<li>Your iron produces a lot of steam. Steam could have condensed on the ironing board cover, making it wet and cause water marks on your garment when the iron is pressed on it.</li>
<li>Ironing aids or fragrance water could have been added to the water tank. This can cause problems with the steaming function, causing water to leak. Use only tap water or distilled water or a mixture of these two types of water.</li>
<li>Steam setting is being used at too low a temperature - The steam function should be switched to "no steam" when using the iron at lower temperatures or the water tank shoud be emptied.</li>
<li>Steam surge/shot/spray being used too rigorously - try using these facilities more slowly.</li>
</ol>
<h4>Water drops appear on the garment during ironing or drip on to the floor.</h4>
<p>The water droplets may appear on the garments when you have steam ironed for a while. This happens because steam has condensed on the ironing board.</p>
<p>Dry the wet spots on the garment and the ironing board cover by passing the iron over them without pressing the steam button. If the underside of the ironing board is wet, wipe it dry.</p>
<p>Make sure you use a mesh-top ironing board with a thick felt material as this minimises steam condensation on the ironing board.</p>
<h4>How can I prevent stains on the soleplate?</h4>
<p>Ironing synthetics at high temperatures can cause staining when synthetic material is scorched.</p>
<p>Synthetics or synthetic-mixed fabrics can only be ironed at the lowest temperature setting.</p>
<p>Always set the temperature acording to the type of clothing being ironed. If unsure, always start with a lower temperature.</p>
<h4>How do I remove the stains on the soleplate?</h4>
<p>To remove a stain, wipe the soleplate with a moist cotton cloth without heating up the iron.</p>
<p>For stubborn stains, soak a cotton rag or pot towel in water &amp; lemon juice, then heat up the iron to 'max' temperature and run the soleplate over the rag.</p>
<h4>Where can I find prices of Russell Hobbs products?</h4>
<p>The price of our products is determined by the current market situation and defined solely by the respective retailer.</p>
<p>A retailer may sell the products at a higher or lower price than the price listed in our Online Shop.</p>
<p>To find prices or buy our latest products, please visit our Online Shop: <a href="http://www.russellhobbs.com">www.russellhobbs.com</a></p>
<p>If the product is not available in our Online Shop, we advise that you visit the websites of your local retailers for price and availability information.</p>
